<h2 class="item1">Настройки скрипта</h2>

<h5>
    Скрипт поддерживает следующие опции для настройки
</h5>

<h3>
    Общие настройки
</h3>

<p>
    Общие настройки скрипта определяются в объекте options при инициализации.
</p>

<h5>mobile</h5>
<dl class="inline-term">
    <dt>Тип</dt>
    <dd>Boolean</dd>
</dl>
<dl class="inline-term">
    <dt>Значение-умолчанию</dt>
    <dd>true</dd>
</dl>

<p>
    Если false, отображает на мобильный устройствах стандартный date picker.
</p>

<h5>days</h5>
<dl class="inline-term">
    <dt>Тип</dt>
    <dd>Array</dd>
</dl>
<dl class="inline-term">
    <dt>Значение-умолчанию</dt>
    <dd>["Su", "Mo", "Tu", "We", "Th", "Fr", "Sa"]</dd>
</dl>

<p>
    Определяет массив названий дней недели для отображения в календаре
</p>

<h5>months</h5>
<dl class="inline-term">
    <dt>Тип</dt>
    <dd>Array</dd>
</dl>
<dl class="inline-term">
    <dt>Значение-умолчанию</dt>
    <dd>["January", "February", "March", "April", "May", "June", "July", "August", "September", "October", "November", "December"]</dd>
</dl>

<p>
    Определяет массив названий месяцев года для отображения в календаре
</p>

<h5>format</h5>
<dl class="inline-term">
    <dt>Тип</dt>
    <dd>String</dd>
</dl>
<dl class="inline-term">
    <dt>Значение по-умолчанию</dt>
    <dd>"mm-dd-yyyy"</dd>
</dl>

<p>
    Определяет формат даты, подставляемый в текстовое поле при выборе даты
</p>

<h5>prevClass</h5>
<dl class="inline-term">
    <dt>Тип</dt>
    <dd>String</dd>
</dl>
<dl class="inline-term">
    <dt>Значение по-умолчанию</dt>
    <dd>"rd-datepicker-prev fa-angle-left"</dd>
</dl>

<p>
    Определяет список классов для кнопки "Передыдущий месяц"
</p>

<h5>nextClass</h5>
<dl class="inline-term">
    <dt>Тип</dt>
    <dd>String</dd>
</dl>
<dl class="inline-term">
    <dt>Значение по-умолчанию</dt>
    <dd>"rd-datepicker-next fa-angle-right"</dd>
</dl>

<p>
    Определяет список классов для кнопки "Следующий месяц"
</p>

<h5>prevText</h5>
<dl class="inline-term">
    <dt>Тип</dt>
    <dd>String</dd>
</dl>
<dl class="inline-term">
    <dt>Значение по-умолчанию</dt>
    <dd>""</dd>
</dl>

<p>
    Определяет текст кнопки "Передыдущий месяц"
</p>

<h5>nextText</h5>
<dl class="inline-term">
    <dt>Тип</dt>
    <dd>String</dd>
</dl>
<dl class="inline-term">
    <dt>Значение по-умолчанию</dt>
    <dd>""</dd>
</dl>

<p>
    Определяет текст кнопки "Следующий месяц"
</p>